    private void estadoBotoes(boolean estado) {
        btNumExcluir.setEnabled(estado);
        btNumAlterar.setEnabled(estado);
        btNumBuscar.setEnabled(estado);
        btVerQuant.setEnabled(estado);
        btLimparLista.setEnabled(estado);
        btGerarTabuada3.setEnabled(estado);
        btVerificar1.setEnabled(estado);
        txtNumPesq.setEnabled(estado);
    }

    private void salvar() {
        if (txtNumSalvar.getText().isEmpty()){
            return;
        }
        int numsalvar = Integer.parseInt(txtNumSalvar.getText());
        boolean status = api.salvar(numsalvar);
        if (status) {
            estadoBotoes(true);
            txtNumSalvar.setText(null);
            txtNumLista.setText(api.listar());
        }
    }

