    private void btNumExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNumExcluirActionPerformed
        boolean status = api.remover(Integer.parseInt(txtNumPesq.getText()));
        if (status) {
            String listanova = api.listar();
            txtNumLista.setText(listanova);
            if (listanova == "sua lista esta vazia") {
                estadoBotoes(false);
            }
        }
    }//GEN-LAST:event_btNumExcluirActionPerformed

    private void btNumAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNumAlterarActionPerformed
        boolean status = api.editar(Integer.parseInt(txtNumPesq.getText()));
        if (status) {
            txtNumLista.setText(api.listar());
        }
    }//GEN-LAST:event_btNumAlterarActionPerformed

